def farenheit_to_celcius(farenheit):
    '''(number) --> float

    Return the number of Celsius degrees for a given farenheit value ("farenheit")

    '''
    return (farenheit - 32) * 5/9

def celcius_to_farenheit(celsius):
    '''(number) --> float

    Return the number of farenheit degrees for a given Celcius value ("celsius")

    '''
    return celsius * 9 / 5 + 32
